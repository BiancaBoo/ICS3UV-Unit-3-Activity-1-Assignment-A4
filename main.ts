// INITIALIZE
const NUMBER1: number = 56.9;
const NUMBER2: number = 89.7;
const NUMBER3: number = 90.2;

// PROCESS
// calculate the average
let answer: number = ((NUMBER1 + NUMBER2 + NUMBER3)/3);

// OUTPUT
// display the result
console.log("The average of " + NUMBER1 + ", " + NUMBER2 + ", and " + NUMBER3 + " is " + answer);

console.log ("\nDone.");
